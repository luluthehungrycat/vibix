//==============================================================================
// fb.rs — VBE Framebuffer Driver (Bochs VBE / QEMU std-vga)
//
// Programmes the Bochs VBE hardware directly via I/O ports to set a linear
// framebuffer mode, then reads the PCI BAR0 of the VGA controller to find
// the LFB physical address.  Maps it into the address space via page-table
// manipulation.
//
// Also provides pixel-level drawing primitives with an embedded 8×16 bitmap
// font.
//==============================================================================

use crate::pmm::PmmAllocator;
use crate::serial::SerialPort;

//==============================================================================
// Bochs VBE I/O interface
//==============================================================================

const VBE_INDEX: u16 = 0x01CE;
const VBE_DATA:  u16 = 0x01CF;

const VBE_REG_ID:     u16 = 0x00;
const VBE_REG_XRES:   u16 = 0x01;
const VBE_REG_YRES:   u16 = 0x02;
const VBE_REG_BPP:    u16 = 0x03;
const VBE_REG_ENABLE: u16 = 0x04;


/// VBE_DISPI_ID5 — signals support for the full feature set (LFB, etc.)
const VBE_ID5: u16 = 0xB0C5;

const VBE_ENABLE_FLAG:       u16 = 0x01;
const VBE_LFB_FLAG:          u16 = 0x40;
const VBE_NOCLEARMEM_FLAG:   u16 = 0x80;

/// Write a Bochs VBE register (index via 0x01CE, data via 0x01CF).
unsafe fn vbe_write(reg: u16, val: u16) {
    core::arch::asm!("outw %ax, %dx", in("ax") reg, in("dx") VBE_INDEX, options(att_syntax));
    core::arch::asm!("outw %ax, %dx", in("ax") val, in("dx") VBE_DATA,  options(att_syntax));
}

/// Read a Bochs VBE register.
unsafe fn vbe_read(reg: u16) -> u16 {
    core::arch::asm!("outw %ax, %dx", in("ax") reg, in("dx") VBE_INDEX, options(att_syntax));
    let val: u16;
    core::arch::asm!("inw %dx, %ax", out("ax") val, in("dx") VBE_DATA,  options(att_syntax));
    val
}

/// PCI configuration-space I/O ports.
const PCI_ADDR_PORT: u16 = 0xCF8;
const PCI_DATA_PORT: u16 = 0xCFC;

/// Read a 32-bit register from PCI configuration space.
unsafe fn pci_config_read(bus: u8, dev: u8, func: u8, reg: u8) -> u32 {
    let addr = 0x80000000u32
        | ((bus as u32) << 16)
        | ((dev as u32) << 11)
        | ((func as u32) << 8)
        | ((reg as u32) & 0xFC);
    core::arch::asm!("outl %eax, %dx", in("eax") addr, in("dx") PCI_ADDR_PORT, options(att_syntax));
    let val: u32;
    core::arch::asm!("inl %dx, %eax", out("eax") val, in("dx") PCI_DATA_PORT, options(att_syntax));
    val
}

//==============================================================================
// Initialisation
//==============================================================================

/// Try to initialise the Bochs VBE framebuffer.
///
/// Returns `None` if the hardware is not present or initialisation fails.
fn init_vbe(pmm: &mut PmmAllocator, serial: &mut SerialPort) -> Option<Framebuffer> {
    // ── Probe for Bochs VBE ────────────────────────────────────────────────
    //
    // Write the ID register with a known value.  If the hardware supports the
    // Bochs VBE interface, reading it back returns >= VBE_ID5.

    let orig_id = unsafe { vbe_read(VBE_REG_ID) };
    unsafe { vbe_write(VBE_REG_ID, VBE_ID5) };
    let probe = unsafe { vbe_read(VBE_REG_ID) };

    // Restore original (harmless if already VBE_ID5).
    unsafe { vbe_write(VBE_REG_ID, orig_id) };

    if probe < VBE_ID5 {
        serial.writestrs(&["VIBIX: fb: Bochs VBE not detected.\n"]);
        return None;
    }

    // ── Set video mode ─────────────────────────────────────────────────────
    //
    // Use 1024×768×32bpp.  Disable before programming (0), then enable with
    // LFB + no-clear flags (to avoid flicker from clearing the whole screen
    // before we've mapped the memory).
    const WIDTH:  u16 = 1024;
    const HEIGHT: u16 = 768;
    const BPP:    u16 = 32;

    serial.writestrs(&["VIBIX: fb: Bochs VBE detected — setting 1024×768×32.\n"]);

    unsafe {
        vbe_write(VBE_REG_ENABLE, 0);               // disable first
        vbe_write(VBE_REG_XRES,   WIDTH);
        vbe_write(VBE_REG_YRES,   HEIGHT);
        vbe_write(VBE_REG_BPP,    BPP);
        vbe_write(VBE_REG_ENABLE, VBE_ENABLE_FLAG | VBE_LFB_FLAG | VBE_NOCLEARMEM_FLAG);
    }

    // Read back the set resolution for verification
    let actual_x = unsafe { vbe_read(VBE_REG_XRES) };
    let actual_y = unsafe { vbe_read(VBE_REG_YRES) };
    let actual_b = unsafe { vbe_read(VBE_REG_BPP) };
    let actual_e = unsafe { vbe_read(VBE_REG_ENABLE) };
    use core::fmt::Write;
    let _ = write!(serial, "VIBIX: fb: VBE status — {}×{}×{} enable={:#x}\n",
        actual_x, actual_y, actual_b, actual_e);

    // ── Read PCI BAR0 of VGA controller ────────────────────────────────────
    //
    // The VGA controller is typically at PCI bus 0, device 2, function 0
    // on QEMU's i440fx chipset.  BAR0 is the LFB physical address.
    let bar0 = unsafe { pci_config_read(0, 2, 0, 0x10) };
    let fb_addr = (bar0 & 0xFFFFFFF0) as u64;   // lower 4 bits are flags

    if fb_addr == 0 {
        serial.writestrs(&["VIBIX: fb: VGA BAR0 is 0 — no LFB.\n"]);
        return None;
    }

    let pitch  = (WIDTH as u32) * 4;     // 32 bpp → 4 bytes per pixel
    let width  = WIDTH as u32;
    let height = HEIGHT as u32;
    let bpp    = BPP as u8;

    let _ = write!(serial, "VIBIX: Framebuffer: {} × {} @ {} bpp, addr {:08x}\n",
        width, height, bpp, fb_addr);

    // ── Map the framebuffer ────────────────────────────────────────────────
    let fb_size = (pitch as u64) * (height as u64);
    map_framebuffer(fb_addr, fb_size as usize, pmm);

    Some(Framebuffer { address: fb_addr, pitch, width, height, bpp })
}

/// High-level init: try VBE, fall back gracefully.
pub fn init(pmm: &mut PmmAllocator, serial: &mut SerialPort) -> Option<Framebuffer> {
    if let Some(fb) = init_vbe(pmm, serial) {
        return Some(fb);
    }
    serial.writestrs(&["VIBIX: No framebuffer available.\n"]);
    None
}

//==============================================================================
// Framebuffer info & drawing primitives
//==============================================================================

pub struct Framebuffer {
    pub address: u64,
    pub pitch: u32,
    pub width: u32,
    pub height: u32,
    #[allow(dead_code)]
    pub bpp: u8,
}

impl Framebuffer {
    /// Plot a single 32-bit pixel at (x, y).
    pub fn plot_pixel(&self, x: u32, y: u32, colour: u32) {
        if x >= self.width || y >= self.height {
            return;
        }
        let offset = (y as u64) * (self.pitch as u64) + (x as u64) * 4;
        unsafe {
            *((self.address + offset) as *mut u32) = colour;
        }
    }

    /// Fill a rectangle with a solid colour.
    pub fn fill_rect(&self, x1: u32, y1: u32, x2: u32, y2: u32, colour: u32) {
        let x1 = x1.min(self.width.saturating_sub(1));
        let y1 = y1.min(self.height.saturating_sub(1));
        let x2 = x2.min(self.width.saturating_sub(1)).max(x1);
        let y2 = y2.min(self.height.saturating_sub(1)).max(y1);

        for y in y1..=y2 {
            let base = self.address + (y as u64) * (self.pitch as u64);
            for x in x1..=x2 {
                unsafe { *((base + (x as u64) * 4) as *mut u32) = colour; }
            }
        }
    }

    /// Draw a character using the built-in 8×16 bitmap font.
    pub fn draw_char(&self, x: u32, y: u32, c: char, colour: u32, bg: Option<u32>) {
        let idx = (c as usize).wrapping_sub(0x20);
        if idx >= FONT8X16.len() {
            return;
        }

        let glyph = FONT8X16[idx];
        for row in 0..16u32 {
            let row_bits = glyph[row as usize];
            for col in 0..8u32 {
                let px = x.wrapping_add(col);
                let py = y.wrapping_add(row);
                if px >= self.width || py >= self.height {
                    continue;
                }
                if row_bits & (0x80 >> col) != 0 {
                    self.plot_pixel(px, py, colour);
                } else if let Some(bg_c) = bg {
                    self.plot_pixel(px, py, bg_c);
                }
            }
        }
    }

    /// Draw a string at pixel position (x, y).  9 px per char (8 + 1 spacing).
    /// Ignores newlines in the string.
    pub fn draw_string(&self, x: u32, y: u32, s: &str, colour: u32, bg: Option<u32>) {
        let mut cx = x;
        for c in s.chars() {
            self.draw_char(cx, y, c, colour, bg);
            cx = cx.wrapping_add(9);
            if cx.wrapping_add(8) >= self.width {
                break;
            }
        }
    }

    /// Clear the entire screen to a colour.
    pub fn clear(&self, colour: u32) {
        self.fill_rect(0, 0, self.width - 1, self.height - 1, colour);
    }
}

//==============================================================================
// Page table manipulation (4-level paging)
//==============================================================================

const PAGE_PRESENT: u64  = 1 << 0;
const PAGE_WRITABLE: u64 = 1 << 1;
const PAGE_HUGE: u64     = 1 << 7;
const PAGE_FLAGS: u64    = PAGE_PRESENT | PAGE_WRITABLE;

/// Identity-map physical 2 MiB pages covering the framebuffer address range.
fn map_framebuffer(fb_phys: u64, fb_size: usize, pmm: &mut PmmAllocator) {
    let start = fb_phys & !0x1FFFFF;
    let end   = (fb_phys + fb_size as u64 + 0x1FFFFF) & !0x1FFFFF;
    let count = ((end - start) / 0x200000) as usize;
    if count == 0 {
        return;
    }

    let cr3: u64;
    unsafe { core::arch::asm!("mov {}, cr3", out(reg) cr3); }
    let l4 = unsafe { &mut *(cr3 as *mut [u64; 512]) };

    for i in 0..count {
        let vaddr = start + (i as u64) * 0x200000;
        let paddr = start + (i as u64) * 0x200000;

        let l4i = ((vaddr >> 39) & 0x1FF) as usize;
        let l3i = ((vaddr >> 30) & 0x1FF) as usize;
        let l2i = ((vaddr >> 21) & 0x1FF) as usize;

        let l3 = get_or_create_table(&mut l4[l4i], pmm);
        let l2 = get_or_create_table(&mut l3[l3i], pmm);

        if l2[l2i] & PAGE_PRESENT != 0 {
            continue;
        }
        l2[l2i] = paddr | PAGE_FLAGS | PAGE_HUGE;

        unsafe {
            core::arch::asm!("invlpg [{v}]", v = in(reg) vaddr, options(nostack, preserves_flags));
        }
    }
}

/// Return the next-level page table (allocating and wiring it if absent).
fn get_or_create_table<'e>(entry: &'e mut u64, pmm: &mut PmmAllocator) -> &'e mut [u64; 512] {
    if *entry & PAGE_PRESENT != 0 {
        let addr = *entry & 0x000FFFFF_FFFFF000;
        return unsafe { &mut *(addr as *mut [u64; 512]) };
    }
    let page = pmm.alloc();
    if page.is_null() {
        loop { unsafe { core::arch::asm!("hlt", options(nomem, nostack)) } }
    }
    let table = unsafe { &mut *(page as *mut [u64; 512]) };
    for slot in table.iter_mut() { *slot = 0; }
    *entry = (page as u64) | PAGE_FLAGS;
    table
}

